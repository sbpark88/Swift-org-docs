import Foundation

// 2. Arithmetic Operators (사칙 연산자)
print("# 1. Arithmetic Operators(+, -, x, /)")
print("Addition:        8 + 2 = \(8 + 2)")
print("Subtraction:     8 - 2 = \(8 - 2)")
print("Multiplication:  8 x 2 = \(8 * 2)")
print("Division:        8 / 2 = \(8 / 2)")
print("\"Hello \" + \"Swift\" = \("Hello " + "Swift")")

// Remainder Operator (나머지 연산자)
print("\n# 2. Remainder Operator")
print("25 % 7 = \(25 % 7)")

// Unary Minus Operator (단항 음수 연산자)
print("\n# 3. Unary Minus Operator")
let three = 3
let minusThree = -three
let plusThree = -minusThree
print("minusThree = -three       >> minusThree is \(minusThree)")
print("plusThree = -minusThree   >> plusThree is \(plusThree)")
